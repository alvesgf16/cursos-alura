from abc import ABCMeta, abstractmethod


class Media(metaclass=ABCMeta):
    def __init__(self, name, year):
        self._name = name.title()
        self.year = year
        self._likes = 0

    @property
    def likes(self):
        return self._likes

    def like(self):
        self._likes += 1

    @property
    def name(self):
        return self._name

    @name.setter
    def name(self, new_name):
        self._name = new_name.title()

    @abstractmethod
    def __str__(self):
        pass
